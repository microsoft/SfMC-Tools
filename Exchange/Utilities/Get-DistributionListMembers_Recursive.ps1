#This script will pull the members of a DL and the members of the nested DL recursively
#This script can run against Exchange Server or Exchange Online
#How to run the script
# 1. Open Exchange Management Shell (EMS) or PowerShell and connect to Exchange Online
# 2. In either EMS or PowerShell, navigate to where the script is located and run it
# 3. The script will prompt you for the DL to target, please enter the DL name
# 4. Once complete, the script will save the exported CSV to the PS profile path named DistributionListMembers_Recursive.csv

$global:RootGroup
$global:Record = @()
$Global:ParentGroup = ""
$Global:TempParentGroup = ""
$Global:NestedGroup = ""
$Global:ColRecords = @()


Function Get-RootGroup{
        $GroupToExpand = Read-Host "Please enter the email address of the Distribution Group you would like to expand"
        Return $GroupToExpand
} 

Function Enumerate-GroupMember {
    Param ($Group)
    $Global:TempParentGroup = $Global:ParentGroup
    $Global:ParentGroup = $Group

    $GroupMembers = Get-DistributionGroupMember -Identity $Group -ResultSize unlimited

    Foreach ($Member in $GroupMembers){
        $global:Record = "" | Select-Object "PrimarySMTPAddress","Displayname","RecipientType","RootGroup","ParentGroup"
        $global:Record.PrimarySMTPAddress = $Member.PrimarySmtpAddress
        $global:Record.Displayname = $member.DisplayName
        $global:Record.RecipientType = $Member.RecipientType
        $global:Record.RootGroup = $global:RootGroup
        $global:Record.ParentGroup = $Global:ParentGroup
    
        $Global:ColRecords += $global:Record
    
        If ($Member.RecipientType -like "*group*"){
            $Global:ParentGroup = $TempParentGroup
            Enumerate-GroupMember $Member.PrimarySmtpAddress
        }
    }


}

$global:RootGroup = Get-RootGroup
Try {Get-DistributionGroup -Identity $RootGroup} Catch {Write-Host "Unable to get the Distribution Group, please re-enter DI's email address" ; Get-RootGroup}

$GroupMembers = Get-DistributionGroupMember -Identity $RootGroup -ResultSize unlimited

Foreach ($Member in $GroupMembers){
    $global:Record = "" | Select-Object "PrimarySMTPAddress","Displayname","RecipientType","RootGroup","ParentGroup"
    $global:Record.PrimarySMTPAddress = $Member.PrimarySmtpAddress
    $global:Record.Displayname = $member.DisplayName
    $global:Record.RecipientType = $Member.RecipientType
    $global:Record.RootGroup = $global:RootGroup
    $global:Record.ParentGroup = $global:RootGroup

    $Global:ColRecords += $global:Record

    If ($Member.RecipientType -like "*group*"){
        
        Enumerate-GroupMember $Member.PrimarySmtpAddress
    }
}

$Global:ColRecords | Export-Csv .\DistributionListMembers_Recursive.csv
